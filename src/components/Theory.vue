    <p>
      В процесі компіляції програми для змінних виділяється пам'ять конкретного розміру, яка зберігається за змінними на
      весь період роботи програми. Такі змінні є статичними, доступ до відповідних областей пам’яті здійснюється за їх
      іменами.
      Такий спосіб не є ефективним, особливо коли невідомо які розміри пам'яті потрібні для зберігання даних. Змінні,
      які з'являються в процесі роботи програми або їх розмір може змінюватись при її роботі називають динамічними
      змінними.
      Для таких даних пам'ять виділяється на етапі виконання програми, крім того, така пам'ять може бути визволеною ще
      до кінця програми.
      Динамічна змінна явно не описується, до неї не­має доступу по імені. Замість цього використо­вується спеціальна
      змінна - вказівник, значенням якого є адреса пам'яті, де роз­міщується динамічний об'єкт.
      Вказівники можна описувати в програмі з допомогою символу ^, наприклад:
    </p>
    <pre>
  Var Vkaz1,Vkaz2:^Integer;
  R,Т:^Rеа1;
</pre>
    <p>
      Такий опис ще не виділяє пам'ять для змінних, він тільки означає, що змінні Vkaz1 та Vkaz2 є вка­зівниками на дані
      цілого типу, а змінні R і Т - вказівники на дійсне дане.
      Виділення пам'яті здійснюється спеціальним опе­ратором New, наприклад:
    </p>
    <pre>
  New(T);
    </pre>

    <p>Після цієї операції вказівник Т придбає значення адреси пам'яті для дійсної змінної. Щоб отримати доступ до цієї
      області пам'яті, треба знак ^ записати після вказівника, наприклад:
    </p>
    <pre>
  Т^;=16.4;
  Writeln(T^);
      </pre>
    <p>

      Над динамічними змінними можна виконувати такі ж операції, як і над статичними, наприклад:
    </p>
    <pre>
  R^:=T^+20;
  If R^<30 Then T^:=40;
</pre>
    <p>
      Вказівники, в свою чергу, можна тільки присвоювати один одному та порівнювати на рівність чи нерівність між собою,
      наприклад:
    </p>
    <pre>
  T:=R;
  If T<>R Then T:=Nil;
  </pre>
    <p>
      Треба бути уважними при вживанні динамічних змінних та вказівників. Розглянемо приклад фрагменту програми:
    </p>
    <pre>
  R^:=10.5;
  T^:=16.2;
  R:=T;
</pre>
    <p>
      Якщо замість останнього оператору вжити опера­тор R^:=Т^, то вказівники будуть зв'язані з різними комірками
      пам'яті, в яких записано одне й теж число 16.2.
      Динамічною змінною може бути також рядок або масив, наприклад:
    </p>
    <pre>
  Type Strichka=String[50];
  Mas=Array[1..40] of Real;
  Var VkazStr:^Strichka;
  VkazMas:^Mas;
    </pre>
    <p>
      Якщо треба виділити пам'ять під масив достатньо виконати оператор:
    </p>
    <pre>
  New(VkazMas)
</pre>
    <p>
      Тепер в циклі можна ввести значення його елементів:
    </p>
    <pre>
  For i:=l to 40 do Read(VkazMas^[i]);
</pre>
    <p>
      Як бачимо, до елементу масиву можна звертатися, вживаючи ім'я VkazMas^[i].
      Якщо в процесі виконання програми яка-небудь змінна стане непотрібною, її можна вилучити з пам'яті за допомогою
      оператора Dispose, наприклад:
    </p>
    <pre>
  Dispose(VkazMas);
</pre>
    <p>
      Це значить, що пам’ять, яка була виділена під масив звільнена, а сам вказівник VkazMas матиме невизначене
      значення.
      Подібна операція буває корисною при необхідності економити пам’ять.
      В мові Pascal можна, також використовувати нетиповані вказівними, які, на відміну від типова них, не зв’язані з
      конкретним типом. Такі вказівники описують, вживаючи слово Pointer, наприклад:
    </p>
    <pre>
  Var Р1, Р2 : Pointer;
</pre>
    <p>
      Ці вказівники сумісні з будь-якими типованими вказівниками, тобто вказівник Р1 може одержати, як значення
      вказівника на ціле число, так і значення вказівника на дійсне число.
      Нетипований вказівник може одержати значення одним з декількох способів.
      ІІроцедура GetMem(P1,Size) виділить пам'ять розміром Size байтів, вказівник Р1 матиме значення адреси початку
      такої пам'яті (величина Size обмеже­на числом 64К). Це нерідко вживають для зберігання фрагментів зображення на
      екрані дисплею. Звільнення такої пам'яті виконує процедура FreeMem(Pl,Size).
      Функція Addr визначає адресу свого аргументу (змінної або підпрограми), наприклад:
    </p>
    <pre>
  P2:=Addr(A);
</pre>
    <p>
      Таку операцію використовують, наприклад, при заміні стандартної підпрограми обробки переривання на «свою»
      підпрограму.
      Функція Ptr перетворить свій аргумент-адресу в значення вказівника, наприклад:
    </p>
    <pre>
  P1:=Ptrt($1234,$4567);
</pre>
    <p> Нетипованому вказівнику можна присвоїти значення спеціальної функції Nil, наприклад:
    </p>
    <pre>
  P1:=NiI;
</pre>
    <p>
      Функція Nil виробляє значення «пусте» (згадайте пустий рядок, пусту множину). Вона застосовується, як деяка ознака
      кінця динамічної структури, напри­клад, кінця списку.
      При програмуванні на мові Pascal для розміщення динамічних змінних використовується область пам'яті з назвою Heap
      або «куча». Розмір Heap можна регулювати за допомогою директиви компілятору $М або задати при налагодженні
      середовища (Option/Compiler/Memory size) від 0 до 65535 байт.
      Приклад директиви:
    </p>
    <pre>

    {$М 20000,10000,200000}
</pre>
    <p>
      Перше число показує розмір сегмента стеку (від 1024 до 655356). Це число вибирається в за­лежності від наявності
      та виду підпрограм. Друге число визначає найменш допустимий в даній прог­рамі розмір Heap, а останнє число -
      найбільший розмір цієї пам'яті. Конкретні розміри Heap виби­раються в залежності від задачі та кількості
      дина­мічних даних.

    </p>
    <p>

      Правила написання Pascal-програм
    </p><p>
1.  Основний текст будь-якої програми починається службовим словом begin і закінчується словом end, після чого слідує крапка. Без крапки програма вважається не закінченою. І навпаки, якщо в програмі знаходиться крапка, то всі команди, що слідують за нею ігноруються (оскільки програма завершена).
  </p><p>

2.  В кінці кожної команди ставиться крапка з комою (“;”) - символ, що розділяє команди між собою.
  </p><p>

3.  Після команди, яка знаходиться перед end, крапку з комою (“;”) бажано не встановлювати, оскільки буде вважатись, що перед end є ще один порожній оператор.
  </p><p>

4.  Команди в Pascal можна записувати в один рядок, хоча для полегшення читабельності програми бажано кожну команду вказувати з нового рядка.
  </p><p>

5.  Якщо користувач бажає помістити в текст програми коментарі, то їх необхідно вказувати у фігурних дужках (замість фігурних дужок можна вказувати альтернативний набір символів   - “(*     ,,,    *)” ).
  </p><p>

6.  При вказанні в програмі виразів, що містять будь-які дужки потрібно пам’ятати, що кількість закритих та відкритих дужок повинна бути однаковою.
  </p><p>

7.  Якщо потрібно використати декілька операторів у якості одного складеного оператора, то їх слід взяти в операторні дужки, що починаються словом begin і закінчуються end. При цьому кількість слів begin у програмі повинна співпадати з кількістю слів end.
  </p><p>

8.  Усі змінні, константи та типи даних, що використовуються в програмі, повинні бути описані в розділах const, type та var.
  </p><p>

Необов'язкові але бажані правила:
  </p><p>

1.  Після команди begin всі наступні команди, аж до відповідного йому end, бажано записувати з відступом (наприклад, на величину слова begin).
  </p><p>

2. Назви всіх команд бажано записувати малими літерами, а змінні - великими.

